export const worker = setupWorker(
  http.get("/pokemons", async () => {
    const data = [...pokemons];
    const result: PokemonDataArray = {
      allPokemonData: data
    };
    return HttpResponse.json(result);
  }),
  http.get("/pokemons/:pokemonId", async ({ params }) => {
    const item = pokemons.find((item) => item.id === params.pokemonId);
    if (item) {
      return HttpResponse.json(item);
    }
    return new HttpResponse(null, { status: 404 });
  }),
  // https://mswjs.io/docs/api/passthrough/
  http.get('https://raw.githubusercontent.com/*', async () => {
    return passthrough();
  }),
  http.get('https://fonts.googleapis.com/*', async () => {
    return passthrough();
  }),
  http.get('https://fonts.gstatic.com/*', async () => {
    return passthrough();
  }),
  http.get('https://assets.pokemon.com/*', async () => {
    return passthrough();
  })
);
